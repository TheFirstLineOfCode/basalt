package com.thefirstlineofcode.basalt.oxm.preprocessing;

public class OutOfMaxBufferSizeException extends Exception {

	private static final long serialVersionUID = -874915375097088032L;

	public OutOfMaxBufferSizeException() {
		super();
	}

	public OutOfMaxBufferSizeException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public OutOfMaxBufferSizeException(String arg0) {
		super(arg0);
	}

	public OutOfMaxBufferSizeException(Throwable arg0) {
		super(arg0);
	}
	
}
