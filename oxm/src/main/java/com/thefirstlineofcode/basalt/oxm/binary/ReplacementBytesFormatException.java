package com.thefirstlineofcode.basalt.oxm.binary;

public class ReplacementBytesFormatException extends RuntimeException {

	private static final long serialVersionUID = 1385030006152070448L;

	public ReplacementBytesFormatException() {
		super();
	}

	public ReplacementBytesFormatException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ReplacementBytesFormatException(String message, Throwable cause) {
		super(message, cause);
	}

	public ReplacementBytesFormatException(String message) {
		super(message);
	}

	public ReplacementBytesFormatException(Throwable cause) {
		super(cause);
	}

}
